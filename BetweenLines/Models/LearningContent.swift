                直接说情感的问题：
                
                我很孤独（太直白，没有画面）
                我非常非常孤独（重复也没用）
                我孤独得要死（夸张但空洞）
                
                用意象表达：
                
                深夜的便利店
                只有我和店员
                他在玩手机
                
                这个画面比任何直接表达都有力量。因为它让读者看到、感受到，而不是被告知。
                
                
