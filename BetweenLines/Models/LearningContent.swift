                高尚是高尚者的墓志铭
                
                克制、冷静，但有力量。
                
                
                记住
                
                避免煽情的核心是：
                • 真诚
                • 克制
                • 简洁
                
                好的诗歌：
                • 不夸大情绪
                • 不堆砌辞藻
                • 不滥用感叹号
                • 用画面代替直说
                
                最深的情感
                往往是最安静的。
                
                不要大喊大叫，
                轻声说，
                反而更有力量。
                
