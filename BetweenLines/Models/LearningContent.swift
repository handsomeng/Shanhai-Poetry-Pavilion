                代表作《面朝大海，春暖花开》（1989），成为最受欢迎的现代诗之一。
                
                特点：纯净、简单、充满理想主义。
                
                ### 余秀华（1976-）
                
                2014 年因《穿过大半个中国去睡你》在网络走红，掀起诗歌热潮。
                
                特点：直接、粗粝、真实。
                
                ### 当代：百花齐放
                
                今天的诗歌没有统一风格，各种流派并存：
                - 口语诗
                - 知识分子写作
                - 下半身写作
                - 网络诗歌
