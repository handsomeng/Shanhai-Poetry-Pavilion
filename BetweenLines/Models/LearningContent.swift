                煽情，就是为了打动读者而故意夸大情绪、使用华丽辞藻、堆砌感叹词。
                
                煽情的特点：
                • 情绪用力过猛
                • 不真实
                • 太刻意
                
                
                为什么要避免煽情
                
                1. 煽情不真实
                
                过度的情绪表达让人觉得假。
                
                比如：
                
                我痛不欲生撕心裂肺肝肠寸断生不如死……
                
                堆砌这么多词，反而不动人。
                
                2. 煽情缺乏力量
                
                真正的情感不需要大喊大叫。
                
                最深的悲伤往往是安静的。
                
                3. 煽情让读者反感
                
                读者能感觉到你在"用力"，反而会抗拒。
                
                
                煽情的表现
                
                表现一：情绪词堆砌
                
                我非常非常非常想你
                想得痛不欲生生不如死撕心裂肺
                
                太满了，失去力量。
                
                改进：
                
                我想你
                像想一场
                一直没下的雨
                
                表现二：滥用感叹号
                
                我爱你！！！
                我真的爱你！！！
                你知道吗！！！
                
                感叹号用多了，反而不强烈。
                
                改进：
                
                我爱你
                
                （简洁，反而有力量）
                
                表现三：华丽辞藻堆砌
                
                我的心如同千万朵玫瑰在燃烧
                我的灵魂在无尽的深渊中哭泣
                我的眼泪汇成了太平洋
                
                太夸张了，不真实。
                
                改进：
                
                眼泪
                一直在打转
                但我
                没让它掉下来
                
                表现四：刻意制造冲突
                
                为了显得悲伤，故意写极端的场景：
                
                我站在悬崖边
                看着下面的深渊
                想要跳下去
                
                如果不是真实经历，就是煽情。
                
                
                如何避免煽情
                
                原则一：真诚第一
                
                写真实的感受，不夸大、不编造。
                
                真实经历：
                你走了
                我没哭
                
                只是晚上
                睡不着
                
                （真实、克制，反而动人）
                
                原则二：克制表达
                
                情感越深，越要克制。
                
                不要：我痛得要死
                
                要：痛
                    像一根刺
                    拔不出来
                
                原则三：用画面代替直说
                
                不要直接说情绪，用画面展示。
                
                不要：我很想你
                
                要：手机屏幕
                    一直是黑的
                
                原则四：删减、删减、再删减
                
                写完后，把夸张的词、多余的感叹号、华丽的辞藻都删掉。
                
                只留下最简洁、最准确的表达。
                
                
                真诚 vs 煽情
                
                对比一：
                
                煽情：
                我的心都要碎了
                我痛不欲生
                我生不如死
                
                真诚：
                痛
                但说不出来
                
                对比二：
                
                煽情：
                我爱你爱得天崩地裂海枯石烂
                
                真诚：
                我爱你
                
                对比三：
                
                煽情：
                我的眼泪如同决堤的洪水倾泻而下
                
                真诚：
                眼泪
                掉下来了
                
                
                实践练习
                
                练习一：删减煽情表达
                
                煽情版：
                我非常非常想你想得撕心裂肺痛不欲生日日夜夜都在想你想得整个世界都失去了颜色
                
                改进：
                我想你
                
                （越简单越有力量）
                
                练习二：克制改写
                
                煽情版：
                你走了我的世界轰然倒塌我的心被撕成了碎片我痛得要死
                
                克制版：
                你走了
                房间里
                很安静
                
                练习三：真实感受
                
                写一个真实的感受，不夸大：
                
                那天
                我一个人去了电影院
                买了两张票
                
                另一张
                没人用
                
                （真实、克制，但很动人）
                
                
                常见错误
                
                错误一：为了煽情而煽情
                
                明明没那么痛苦，但为了写诗夸大情绪。
                
                读者能感觉到假。
                
                解决：写真实的感受。
                
                错误二：不相信简单的力量
                
                觉得简单的表达没有力量，所以堆砌辞藻。
                
                其实，最简单的表达往往最有力量。
                
                错误三：模仿别人的煽情
                
                看到别人写得很"激烈"，就模仿。
                
                但别人的经历不是你的经历。
                
                只写你自己的感受。
                
                
                高级技巧：以静写动
                
                越是激烈的情绪，越要用平静的语气。
                
                例子：
                
                你说分手的时候
                我正在喝咖啡
                
                我说好
                
                然后把咖啡喝完了
                
                （表面平静，但读者能感受到底下的波涛）
                
                
                高级技巧：留白
                
                不把情绪说透，让读者自己感受。
                
                例子：
                
                你走了
                
                （空行）
                
                我还在
                
                （什么情绪？不说，但读者能感受到）
                
                
                高级技巧：用细节代替情绪
                
                不说情绪，只展示细节。
                
                例子：
                
                不说：我很孤独
                
                展示：
                外卖
                只点一人份
                
                
                著名例子
                
                顾城《一代人》：
                
                黑夜给了我黑色的眼睛
                我却用它寻找光明
                
                没有感叹号，没有夸张，简洁有力。
                
